def add():
	num1 = eval(input("Enter a number: "))
	num2 = eval(input("Enter another number: "))
	return num1 + num2


def subtract(num1, num2):
	return num1 - num2

def product(num1, num2):
	return num1 * num2

def divide(num1, num2):
	return num1 / num2

def square(num):
	return num * num

def square_root(num):
	return num ** 0.5

def raise_to_power(num1, num2):
	return num1 ** num2